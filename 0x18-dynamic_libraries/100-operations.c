int add(int a, int b);
int sub(int a, int b);
int mul(int a, int b);
int div(int a, int b);
int mod(int a, int b);

/**
 * add - addation function
 *
 * @a: First number
 * @b: Second number
 *
 * Return: addation of two number
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract function
 *
 * @a: First number
 * @b: Second number
 *
 * Return: subtract a from b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply function
 *
 * @a: First number
 * @b: Second number
 *
 * Return: multiply of two number
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divition function
 *
 * @a: First number
 * @b: Second number
 *
 * Return: divition of a to b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus function
 *
 * @a: First number
 * @b: Second number
 *
 * Return: modulus of a to b
 */
int mod(int a, int b)
{
	return (a % b);
}
