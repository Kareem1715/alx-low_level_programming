#include "3-calc.h"

/**
 * op_add - add function
 *
 * @a: The first number.
 * @b: The second number.
 *
 * Description: This function add two number.
 *
 * Return: summation of two number.
 */
int op_add(int a, int b) { return (a + b); }

/**
 * op_sub - sub function
 *
 * @a: The first number.
 * @b: The second number.
 *
 * Description: This function subtract two number.
 *
 * Return: subtraction of two number.
 */
int op_sub(int a, int b) { return (a - b); }

/**
 * op_mul - mul function
 *
 * @a: The first number.
 * @b: The second number.
 *
 * Description: This function multiplicate two number.
 *
 * Return: multipication of two number.
 */
int op_mul(int a, int b) { return (a * b); }

/**
 * op_div - div function
 *
 * @a: The first number.
 * @b: The second number.
 *
 * Description: This function get the divition of two number.
 *
 * Return: divition of two number.
 */
int op_div(int a, int b) { return (a / b); }

/**
 * op_mod - mod function
 *
 * @a: The first number.
 * @b: The second number.
 *
 * Description: This function get the module of two number.
 *
 * Return: moudule of two number.
 */
int op_mod(int a, int b) { return (a % b); }
